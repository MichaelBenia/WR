const skuToProductMap = new Map(
  PRODUCT_CATALOG.map(product => [normalizeCatalogSku(product.sku), product]),
);

const fixedProductOrderIndex = new Map();
const categoryItemCounts = new Map();
PRODUCT_CATALOG.forEach((product, globalIndex) => {
  const sku = normalizeCatalogSku(product.sku);
  const categoryIndex = CATEGORY_ORDER.indexOf(product.category);
  const itemIndex = categoryItemCounts.get(product.category) || 0;
  categoryItemCounts.set(product.category, itemIndex + 1);
  product.orderIndex = itemIndex;
  product.globalOrderIndex = globalIndex;
  fixedProductOrderIndex.set(sku, {
    category: product.category,
    categoryIndex,
    itemIndex,
    globalIndex,
  });
});

function normalizeCatalogSku(value) {
  let text = String(value ?? "").trim();
  if (/^\d+\.0$/.test(text)) text = text.slice(0, -2);
  return text.replace(/[^a-zA-Z0-9]/g, "").toUpperCase();
}
